import { prisma } from "@/lib/prisma";
import type { LevelConfig } from "@/types/level";

export async function saveCandidateAsGeneratedLevel(candidateId: string) {
  const candidate = await prisma.autoGeneratedLevelCandidate.findUnique({ where: { id: candidateId } });
  if (!candidate) throw new Error("候选不存在");
  const level = JSON.parse(candidate.levelJson) as LevelConfig;
  const validation = candidate.validationJson ? JSON.parse(candidate.validationJson) : null;
  const status =
    validation && Array.isArray((validation as { errors?: unknown[] }).errors) && (validation as { errors: unknown[] }).errors.length > 0
      ? "needs_review"
      : "validated";
  const created = await prisma.generatedLevel.create({
    data: {
      name: candidate.levelName,
      levelIndex: candidate.targetLevelIndex,
      theme: level.theme,
      itemSetId: level.source.itemSetId,
      itemSetName: level.source.itemSetId,
      assetBatchId: level.source.assetBatchId,
      assetBatchName: level.source.assetBatchId,
      timeLimitSec: level.rules.timeLimitSec,
      slotCount: level.rules.slotCount,
      boardWidth: level.board.width,
      boardHeight: level.board.height,
      layerCount: level.board.layerCount,
      targetDifficulty: level.rules.targetDifficulty,
      generatorRuleId: level.rules.generatorRuleId,
      refreshRuleId: level.rules.refreshRuleId,
      levelJson: candidate.levelJson,
      summary: candidate.aiReason ?? "Auto Level Generator 保存",
      warningsJson: JSON.stringify([]),
      validationJson: candidate.validationJson ?? undefined,
      status,
    },
  });
  await prisma.autoGeneratedLevelCandidate.update({
    where: { id: candidateId },
    data: { status: "saved", savedGeneratedLevelId: created.id },
  });
  return created;
}
