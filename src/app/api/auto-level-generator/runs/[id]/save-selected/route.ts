import { NextResponse } from "next/server";

import { saveCandidateAsGeneratedLevel } from "@/lib/auto-level/saveCandidateAsGeneratedLevel";
import { prisma } from "@/lib/prisma";

type Params = { params: Promise<{ id: string }> };

export async function POST(_: Request, { params }: Params) {
  try {
    const { id } = await params;
    const selected = await prisma.autoGeneratedLevelCandidate.findMany({
      where: { runId: id, status: "selected" },
      orderBy: [{ targetLevelIndex: "asc" }, { candidateRank: "asc" }],
    });
    const saved = [];
    for (const row of selected) {
      try {
        saved.push(await saveCandidateAsGeneratedLevel(row.id));
      } catch {
        // 单个失败不影响整体
      }
    }
    return NextResponse.json({ success: true, data: { count: saved.length, saved } });
  } catch (error) {
    const message = error instanceof Error ? error.message : "批量保存已选候选失败";
    return NextResponse.json({ success: false, error: message }, { status: 400 });
  }
}
