import { NextResponse } from "next/server";

import { saveCandidateAsGeneratedLevel } from "@/lib/auto-level/saveCandidateAsGeneratedLevel";
import { prisma } from "@/lib/prisma";

type Params = { params: Promise<{ id: string }> };

export async function POST(_: Request, { params }: Params) {
  try {
    const { id } = await params;
    const candidates = await prisma.autoGeneratedLevelCandidate.findMany({
      where: { runId: id, status: { in: ["candidate", "selected", "needs_review"] } },
      orderBy: [{ targetLevelIndex: "asc" }, { distance: "asc" }, { candidateRank: "asc" }],
    });
    const map = new Map<number, string>();
    candidates.forEach((c) => {
      if (!map.has(c.targetLevelIndex)) map.set(c.targetLevelIndex, c.id);
    });
    const saved = [];
    for (const candidateId of map.values()) {
      try {
        saved.push(await saveCandidateAsGeneratedLevel(candidateId));
      } catch {
        // 单个失败不影响整体
      }
    }
    return NextResponse.json({ success: true, data: { count: saved.length, saved } });
  } catch (error) {
    const message = error instanceof Error ? error.message : "批量保存最佳候选失败";
    return NextResponse.json({ success: false, error: message }, { status: 400 });
  }
}
