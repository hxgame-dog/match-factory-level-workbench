import { NextResponse } from "next/server";

import { prisma } from "@/lib/prisma";

type Params = { params: Promise<{ id: string }> };

export async function POST(_: Request, { params }: Params) {
  try {
    const { id } = await params;
    const candidate = await prisma.autoGeneratedLevelCandidate.findUnique({ where: { id } });
    if (!candidate) return NextResponse.json({ success: false, error: "候选不存在" }, { status: 404 });
    await prisma.autoGeneratedLevelCandidate.updateMany({
      where: { runId: candidate.runId, targetLevelIndex: candidate.targetLevelIndex, status: "selected" },
      data: { status: "candidate" },
    });
    const updated = await prisma.autoGeneratedLevelCandidate.update({ where: { id }, data: { status: "selected" } });
    return NextResponse.json({ success: true, data: updated });
  } catch (error) {
    const message = error instanceof Error ? error.message : "选择候选失败";
    return NextResponse.json({ success: false, error: message }, { status: 400 });
  }
}
